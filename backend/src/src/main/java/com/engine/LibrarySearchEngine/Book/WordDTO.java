public class WordDTO {
    private String word;
    private Double tf_idf;

    public WordDTO() {
        // Constructeur par d√©faut
    }

    /*************************************** Constructors *********************************************************/

    public WordDTO(String word, Double tf_idf) {
        this.word = word;
        this.tf_idf = tf_idf;
    }

    /*************************************** Getters and Setters  ***************************************************/

    public String getWord() {
        return word;
    }

    public void setWord(String word) {
        this.word = word;
    }

    public Double get_tf_idf() {
        return tf_idf;
    }

    public void set_tf_idf(Double tf) {
        this.tf_idf = tf;
    }
}
